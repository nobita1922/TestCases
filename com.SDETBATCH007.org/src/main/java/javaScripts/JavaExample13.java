package javaScripts;

public class JavaExample13{
	
	public static void main(String[] args){
		
	int a = 10;
	System.out.println(a);
		int b = ++a;
	System.out.println(b);
		int c = b++;
	System.out.println(c);
		int h = 20;
	System.out.println(h);
		int o = --h;
	System.out.println(o);
		int n = o--;
	System.out.println(n);
		
	}
}
